const mealHandler = (fetchedList) => {
  const foodList = fetchedList.list;
  const startingIndexes = [];

  foodList.forEach((title, index) => {
    //Add to AllList if it is not exist

    if (!title.includes("empty")) {
      if (allList.findIndex((meal) => meal.title === title) === -1) {
        allList.push(new Meal(allList.length + 1, title, false));
      }
    }

    //Find Starting Indexes of Daily Lists
    if (title.includes("Çorba") || title.includes("empty")) {
      startingIndexes.push(index);
    }
  });

  const monthlyList = [];

  for (let i = 0; i < startingIndexes.length; i++) {
    const dailyListTitles = foodList.slice(startingIndexes[i], startingIndexes[i + 1]);
    const dailyList = [];

    dailyListTitles.forEach((mealTitle) => {
      const indexInAllList = allList.findIndex((item) => item.title === mealTitle);
      indexInAllList !== -1 ? dailyList.push(allList[indexInAllList]) : dailyList.push({ title: "empty" });
    });

    monthlyList.push(dailyList);
  }

  // for (let i = 0; i < foodList.length; i += 6) {
  //   const dailyList = [];
  //   for (let j = i; j < i + 6; j++) {
  //     const indexInAllList = allList.findIndex((meal) => meal.title === foodList[j]);

  //     dailyList.push(allList[indexInAllList]);
  //   }
  //   monthlyList.push(dailyList);
  // }
  // console.log(monthlyList);
  return monthlyList;
};

