const mealHandler = (fetchedList) => {
  //Add to AllList if it is not exist

  const foodList = fetchedList.list;

  foodList.forEach((title) => {
    if (allList.findIndex((meal) => meal.title === title) === -1) {
      allList.push(new Meal(allList.length + 1, title, false));
    }
  });

  const monthlyList = [];

  for (let i = 0; i < foodList.length; i += 6) {
    const dailyList = [];
    for (let j = i; j < i + 6; j++) {
      const indexInAllList = allList.findIndex((meal) => meal.title === foodList[j]);

      dailyList.push(allList[indexInAllList]);
    }
    monthlyList.push(dailyList);
  }
  return monthlyList;
};

